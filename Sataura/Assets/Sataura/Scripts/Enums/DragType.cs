namespace Sataura
{
    /// <summary>
    /// Represents the type of drag operation to perform.
    /// </summary>
    public enum DragType
    {
        /// <summary>
        /// The drag operation will swap the dragged item with the target item.
        /// </summary>
        Swap,

        /// <summary>
        /// The drag operation will hold the dragged item over the target item.
        /// </summary>
        Hold
    }
}


